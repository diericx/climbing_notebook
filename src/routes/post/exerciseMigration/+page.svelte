<h1 class="mb-4">New Exercise System</h1>

<p class="">
  Hello!

  <br />
  <br />

  I have recently implemented a new exercise system. Rather than just typing anything for an
  exercise we can now select from a list of actual exercises. This list is updated and maintained by
  the community.

  <br />
  <br />

  The benefit of this system is that exercises can now have meaningful metadata such as targeted
  muscle groups, exercise type (climbing, cardio, strength, etc.) and the ability to alter the forms
  by including/excluding fields. Consequently, this makes it much easier to create charts that query
  exercise data as we are now connecting to actual exercise objects rather than just searching on
  strings. Eventually it will be possible to create a chart showing load on a specific muscle group!

  <br />
  <br />

  My main goal with this is to eventualy enable the community to build and share charts and
  dashboards with each other. By using a set library of exercises, charts and widgets that are
  created by one person have a much higher chance of being relevant to another user.

  <br />
  <br />

  I apologize for the odious migration process, but I promise it is worth it!

  <br />
  <br />

  - Zac
</p>
